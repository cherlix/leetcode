class MedianFinder {
public:
    vector<int> buff;
    /** initialize your data structure here. */
    MedianFinder() {
        
    }
    
    void addNum(int num) {
        auto it = lower_bound(buff.begin(), buff.end(), num);
        buff.insert(it, num);
    }
    
    double findMedian() {
        if (buff.size() == 0) {
            return 0.0;
        }
        
        if (buff.size() & 0x1) {
            return buff[buff.size() / 2];
        } else {
            return (buff[buff.size() / 2 - 1] + buff[buff.size() / 2]) / 2.0;
        }
    }
};

